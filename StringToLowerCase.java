package com.chainsys.day4;

public class StringToLowerCase {

	public static void main(String[] args) {

		String  str= "Welcome";
		System.out.println(str);
		System.out.println(str.toLowerCase());//Converts all of the characters in this String to lower case using the rules of the default locale.
		System.out.println(str.toUpperCase());//Converts all of the characters in this String to upper case using the rules of the default locale.
	}

}
